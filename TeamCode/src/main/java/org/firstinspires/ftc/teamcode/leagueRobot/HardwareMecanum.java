/**
 * This is NOT an opmode.
 *
 * This class can be used to define all the specific hardware for a single robot.
 * In this case that robot is a Pushbot.
 * See PushbotTeleopTank_Iterative and others classes starting with "Pushbot" for usage examples.
 *
 * This hardware class assumes the following device names have been configured on the robot:
 * Note:  All names are lower case and some have single spaces between words.
 *
 * Motor channel:  Left  drive motor:        "left_drive"
 * Motor channel:  Right drive motor:        "right_drive"
 * Motor channel:  Manipulator drive motor:  "left_arm"
 * Servo channel:  Servo to open left claw:  "left_hand"
 * Servo channel:  Servo to open right claw: "right_hand"
 */

@Disabled
public class HardwareMecanum
{/* Public OpMode members. */
    public DcMotor  frontLeft   = null;
    public DcMotor  frontRight  = null;
    public DcMotor  backLeft   = null;
    public DcMotor  backRight  = null;

    public DcMotor leftLift = null;
    public DcMotor rightLift = null;
    public Servo    leftFound    = null;
    public Servo    rightFound   = null;
    public Servo    leftStone    = null;
    public Servo    rightStone   = null;
    public ColorSensor sensorColorFront = null;
    public ColorSensor sensorColorMiddle = null;
    public BNO055IMU imu;
    public DistanceSensor sensorRange;

    /* local OpMode members. */
    HardwareMap hwMap           =  null;
    Telemetry telemetry;
    LinearOpMode opMode;

    /* Constructor */
    public HardwareMecanum(){
    }

    /* Initialize standard Hardware interfaces */
    public void init(HardwareMap ahwMap) {
        // Save reference to Hardware map
        hwMap = ahwMap;

        // Define and Initialize Motors

        frontLeft  = hwMap.get(DcMotor.class,"front_left");
        frontRight = hwMap.get(DcMotor.class,"front_right");
        backLeft    = hwMap.get(DcMotor.class,"back_left");
        backRight    = hwMap.get(DcMotor.class,"back_right");
        leftFound = hwMap.get(Servo.class,"left_found");
        rightFound = hwMap.get(Servo.class,"right_found");
        leftStone = hwMap.get(Servo.class,"left_stone");
        rightStone = hwMap.get(Servo.class,"right_stone");
        leftLift = hwMap.get(DcMotor.class,"left_lift");
        rightLift = hwMap.get(DcMotor.class,"right_lift");

       // leftLift = hwMap.get(DcMotor.class,"left_lift");
       // rightLift = hwMap.get(DcMotor.class,"right_lift");
       // sensorColorFront = hwMap.get(ColorSensor.class, "Fsensor_color_distance");
        sensorColorMiddle = hwMap.colorSensor.get("Msensor_color_distance");
        imu = hwMap.get(BNO055IMU.class,"imu");

        sensorRange = hwMap.get(Rev2mDistanceSensor.class,"sensor_range");

        // you can also cast this to a Rev2mDistanceSensor if you want to use added
        // methods associated with the Rev2mDistanceSensor class.
        Rev2mDistanceSensor sensorTimeOfFlight = (Rev2mDistanceSensor)sensorRange;

        BNO055IMU.Parameters parameters = new BNO055IMU.Parameters();
        parameters.angleUnit = BNO055IMU.AngleUnit.DEGREES;
        parameters.accelUnit = BNO055IMU.AccelUnit.METERS_PERSEC_PERSEC;
        parameters.calibrationDataFile = "BNO055IMUCalibration.json"; //note: run the calibration mode in SensorBNO055IMUCalibration
        parameters.loggingEnabled = true;
        parameters.loggingTag = "IMU";
        parameters.accelerationIntegrationAlgorithm = new JustLoggingAccelerationIntegrator();
        imu.initialize(parameters);

        frontLeft.setMode(DcMotor.RunMode.RUN_USING_ENCODER);
        frontRight.setMode(DcMotor.RunMode.RUN_USING_ENCODER);
        backLeft.setMode(DcMotor.RunMode.RUN_USING_ENCODER);
        backRight.setMode(DcMotor.RunMode.RUN_USING_ENCODER);
        leftLift.setMode(DcMotor.RunMode.RUN_USING_ENCODER);
        rightLift.setMode(DcMotor.RunMode.RUN_USING_ENCODER);
        leftLift.setDirection(DcMotorSimple.Direction.REVERSE);
        backRight.setDirection(DcMotorSimple.Direction.REVERSE);
        frontRight.setDirection(DcMotorSimple.Direction.REVERSE);
        frontLeft.setZeroPowerBehavior(DcMotor.ZeroPowerBehavior.BRAKE);
        frontRight.setZeroPowerBehavior(DcMotor.ZeroPowerBehavior.BRAKE);
        backLeft.setZeroPowerBehavior(DcMotor.ZeroPowerBehavior.BRAKE);
        backRight.setZeroPowerBehavior(DcMotor.ZeroPowerBehavior.BRAKE);
        leftLift.setZeroPowerBehavior(DcMotor.ZeroPowerBehavior.BRAKE);
        rightLift.setZeroPowerBehavior(DcMotor.ZeroPowerBehavior.BRAKE);

        // Set all motors to zero power
        frontLeft.setPower(0);
        frontRight.setPower(0);
        backLeft.setPower(0);
        backRight.setPower(0);
        leftLift.setPower(0);
        rightLift.setPower(0);

    }
}

